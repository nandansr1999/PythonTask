def circle_area(radius):
    r = 3.14*radius*radius
    print(f"The area of the circle is: {r}")

def circumfer(radius):
    r = 2*3.14*radius
    print(f"The circumference of the circle : {r}")

def rectangle_area(l,w):
    a = l * w
    print(f"The area of rectangle is : {a}")

def perimeter(l,w):
    p = 2*(l+w)
    print(f"The perimeter of the rectangle is : {p}")